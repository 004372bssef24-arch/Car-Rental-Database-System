-- ADVANCED OBJECT 1: DATABASE VIEW
-- Business Use: Simplifies a 5-table join to instantly view all active rentals.
-- ==============================================================================

CREATE VIEW vw_ActiveRentalDetails AS
SELECT 
    r.RentalID,
    c.Name AS CustomerName,
    c.Email,
    car.Model,
    car.RegistrationNumber,
    r.RentalDate,
    r.ReturnDate,
    s.Name AS ProcessedBy_Agent
FROM Rental r
INNER JOIN Customer c ON r.CustomerID = c.CustomerID
INNER JOIN Rental_Car rc ON r.RentalID = rc.RentalID
INNER JOIN Car car ON rc.CarID = car.CarID
INNER JOIN Staff s ON r.StaffID = s.StaffID
WHERE r.Status = 'Active';

