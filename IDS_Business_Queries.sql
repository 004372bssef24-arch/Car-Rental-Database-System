-- Query 9: Date Functions & Filtering
-- Business Question: "Which vehicle insurance policies are expiring in the year 2026 and need to be flagged for renewal by the fleet manager?"
SELECT 
    ip.Provider,
    c.Model,
    c.RegistrationNumber,
    ip.ExpiryDate,
    ip.CoverageAmount
FROM Insurance_Policy ip
INNER JOIN Car c ON ip.CarID = c.CarID
WHERE YEAR(ip.ExpiryDate) = 2026
ORDER BY ip.ExpiryDate ASC;

-- Query 10: MAX / Complex Analytics
-- Business Question: "What was the single largest payment amount we have received, and which rental transaction and customer was it associated with?"
SELECT TOP 1
    p.Amount AS LargestPayment,
    p.PaymentDate,
    p.PaymentMethod,
    c.Name AS CustomerName,
    r.RentalID
FROM Payment p
INNER JOIN Rental r ON p.RentalID = r.RentalID
INNER JOIN Customer c ON r.CustomerID = c.CustomerID
ORDER BY p.Amount DESC;

